/*function that reverse a string*/
char *rev_string(char *str)
{
  int i = 0, j = 0, count = 0;
  char t[50];
  while(str[count] != '\0')
    {
      count++;
     
    }
  i = count - 1;
  while(i >= 0)
    {
      t[j] = str[i];
      i--;
      j++;
    }
 
   while(i < count)
    {
      str[i] = t[i];
      i++;
      }
  return str;
}
